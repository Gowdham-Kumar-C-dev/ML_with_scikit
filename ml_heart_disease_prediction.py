data=pd.read_csv('synthetic_heart_disease_dataset.csv')

df=pd.DataFrame(data)

df

df.isnull().sum()

df.fillna(df['Alcohol_Intake'],inplace=True)



df['Gender'].replace({'Male':1,'Female':0},inplace=True)

df

df['Alcohol_Intake'].replace({'Low':0,'Moderate':1,'High':2},inplace=True)

df['Smoking'].replace({'Current':0,'Former':1,'Never':2},inplace=True)

df['Diet'].replace({'Unhealthy':0,'Average':1,'Healthy':2},inplace=True)

df['Physical_Activity'].replace({'Sedentary':0,'Moderate':1,'Active':2},inplace=True)

df

df['Stress_Level'].replace({'Low':0,'Medium':1,'High':2},inplace=True)

df

df.info()

x=df[['Age','BMI','Smoking','Alcohol_Intake','Physical_Activity','Diet','Stress_Level','Previous_Heart_Attack','Cholesterol_Total']]

y=df['Heart_Disease']

x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2)

model=DecisionTreeClassifier(random_state=42)
model.fit(x_train,y_train)

y_pre=model.predict(x_test)

acc=accuracy_score(y_test,y_pre)

print(acc)

